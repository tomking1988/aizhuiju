package com.douban.models;

public class SimpleCelebrity {

	private String id;
	private String name;
	private String alt;
	private String avatars;
	
	public String getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAlt() {
		return alt;
	}
	
	public String getAvatars() {
		return avatars;
	}
}
